// To declare a constant (unchanging) variable, use const instead of let:

const myBirthday = '18.04.1982';
// Variables declared using const are called “constants”. 
// They cannot be reassigned. An attempt to do so would cause an error
const myBirthday = '18.04.1982';//TRIGGER THE ERROR FOR REDECLARATION
myBirthday = '01.01.2001'; //TRIGGER THE ERROR FOR REASSIGNING
